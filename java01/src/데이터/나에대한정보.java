package 데이터;

public class 나에대한정보 {

	public static void main(String[] args) {
		//기본데이터를 사용해서
		//나에 대한 정보를 RAM에 저장
		//정리해서 모니터에 출력
		//키보드의 insert키를 누르면 삽입모드!
		//char name = "홍길동";
		int age = 100; //대입연산자
		char gender = '남';
		double weight = 99.9;
		boolean rain = true;
		//+: 결합연산자
		System.out.println("나이: " + age + "세");
		System.out.println("성별: " + gender);
		System.out.println("몸무게: " + weight + "kg");
		System.out.println("비: " + rain );
		String name = "홍길동";
		System.out.println("이름: " + name);
		
	}

}




