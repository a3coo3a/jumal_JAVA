package 연산자;

public class 산술연산자 {

	public static void main(String[] args) {
		// 산술연산자(+ - * / %:나머지
		int x = 55;
		int y = 40;
		System.out.println(x + "+" + y + " = " + (x + y));
		System.out.println(x + "-" + y + " = " + (x - y));
		System.out.println(x + "*" + y + " = " + (x * y));
		System.out.println(x + "/" + y + " = " + (x / y));
		System.out.println(x + "%" + y + " = " + (x % y));

	}

}
